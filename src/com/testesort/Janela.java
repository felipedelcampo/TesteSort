    //Gera os teste e liga os resultados no HashMap aos labels na tela.
    private void geraTestes(Long tamanhoEntrada){
        Testes testes = new Testes();
        HashMap<String, String> resultados;
        
        resultados = testes.testaSort(tamanhoEntrada);
        
        cSRandom.setText(resultados.get("cSRandom"));
        qSRandom.setText(resultados.get("qSRandom"));
        qSMelhor.setText(resultados.get("qSMelhor"));
        qSPior.setText(resultados.get("qSPior"));
        hSRandom.setText(resultados.get("hSRandom"));
        hSMelhor.setText(resultados.get("hSMelhor"));
        mSRandom.setText(resultados.get("mSRandom"));
        mSMelhor.setText(resultados.get("mSMelhor"));
                
    }

