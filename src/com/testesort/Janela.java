    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        // TODO add your handling code here:
                
        Long tamanhoEntrada = new Long(0);
        //Verifica se o valor da entrada é um número maior que 0
        try {
            tamanhoEntrada = Long.parseLong(tamanhoEntradaForm.getValue().toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Digite somente números", "Erro de tipo de dados", JOptionPane.ERROR_MESSAGE);
        }
        
        if(tamanhoEntrada > 0){
            
            status.setText("Testando");
            geraTestes(tamanhoEntrada);
            status.setText("Terminado");
            
        } else {
            JOptionPane.showMessageDialog(this, "Digite um valor válido no campo", "Erro de validação", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

